    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser f = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("csv & CSV", "csv");
        f.setFileFilter(filter);
        int op = f.showOpenDialog(f);
        if (op == JFileChooser.APPROVE_OPTION) {
            File selectedFile = f.getSelectedFile();
            LeerArchivoCsv csv = new LeerArchivoCsv(selectedFile.getAbsolutePath());
            try {
                Vector datos = csv.procesar();
                JOptionPane.showMessageDialog(null, "Archivo Importado");
                System.out.println(datos);
            } catch (Exception e) {
                System.out.print("####### ERROR #######");
                System.err.print(e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "La extensi√≥n debe ser CSV.", "Debe seleccionar un archivo con extension csv.", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

